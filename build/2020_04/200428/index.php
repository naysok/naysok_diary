<h1>200428</h1>

<p>昼に起きて仕事を。  </p>

<p>git 環境を整っていない相手に対して、ばらばらのモジュールをひとまとめのプログラムにまとめて送るというのが、バグのリスク等々、結構手間だなと感じてしまう。ベストソリューションを見つけたい。  </p>

<p>夜は、外のエンジニアの人が作ったアプリケーションを見る。お前の立場は何？的な言い方になるが、とても良く出来ていて勉強になった。  </p>

<p>仕事中、ちょうど連絡をしたいなと思っていた先輩から連絡が来て、近いフィーリングを共有できて嬉しかった。成果はどうであれ研究をがんばるんやという気持ちで取り組んだ経験は少しずつ身になっているし、研究室での経験をこれからも度々思い出すのだろうなと思う。  </p>

<p>深夜、苦労している友人に適当な手助けを。お礼は、という話をされ、身の回りの上の人やインターネットの向こうの人からの無償の施しを受けてきた身として僕が貰うのはおかしい的な返事をしたが、専門的な知見に対しては対価を払うのは当たり前と返される。  </p>

<p>確かに、と納得しつつも、先人からの多大な施しにより今の自分があるので、先人からやってもらった通りに困っている人を手助けしていかなければならないという気持ちもある。  </p>

<p>たぶんどちらも正しい振る舞いであるが、ここまで先人からの多大な施しを受けてきた身としては、僕自身は無償での手助けをすることを止めてはいけないと思う。  </p>

