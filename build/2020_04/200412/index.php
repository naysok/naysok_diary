<h3>所感</h3>

<p>朝起きて、洗濯などしつつ、近所のカレー屋へ。そのあと、ちょっと遠いスーパーへ買い出し。パスタと、牛乳やヨーグルトを買う。ちょっと遠いスーパーは幸町という交差点の近くにあるが、千葉市美浜区幸町という地名を、（さちまち）かと思っていたが、（さいわいちょう）と知った。  </p>

<p>家から出る際に、一応マスクをつけるようにしているが、家にある使い捨てマスクが残り10枚程度になっている。最低限の買い出しのための週に1度か2度の外出は避けれないので、大事につかっても残り1ヶ月分しかない。  </p>

<p>マスクがいつ手に入るかわからないので、楽天ショッピングで、適当な家電の店から買う。50枚入り、送料込みに、謎の割引が使えて、4380円だった。12～15営業日以内に発送と書いてあるが届くのだろうか。  </p>

<p>今日もオフと割り切って雑なコードを書く。メモリオーバーに遭遇し、import gc / gc.collect() で、ガベージコレクションを行うことで、メモリ解放できるということを知った。いつか役に立てばいいな。  </p>

