<h3>雑感</h3>

<p>朝起きて、市川のコーナンに在庫確認、取り置き依頼の電話をする。急いで外に出るも雨が降っていた。西船橋から市川原木のコーナンまで2キロくらいで、いつもなら歩いてたと思うが雨だし急ぎだしとタクシーに。コーナンにて必要なものをピックして、二俣新町経由で東京へ。東京駅前のヤマダで必要なものを買って京橋の、東京メイカーへ向かう。各種機材が揃っていて感動した。  </p>

<p>加工を終え池尻に荷物を届ける。適当な作業とフルカラーの造形の確認をして帰宅。久しぶりに歩いたからか、足の裏が痛い気がするので家に帰ってだらだらして寝る。明日から仕事を頑張ろうと思う。  </p>

