<p>今日は話していないが、僕は、先人がソフトハード共に貧弱な時代に荒野を切り開いて整備してくれた遊歩道に、我々はタダ乗っているだけ。先人に比べて、我々ツールユーザ世代は、腕力が違いすぎるのでまじで頑張らないといけない、的な話も最近色々なところで繰り返し口にしている気がする。これは本当に行動に移していきたい。  </p>

