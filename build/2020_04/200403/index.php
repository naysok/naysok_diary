<h3>フルカラープリント</h3>

<p>連番画像による擬似ボクセルが必要。ライノで、ターゲットのメッシュの切断線を求めて、連番画像書き出しを想定しているが、これではボクセルの隠せるの W / H / D のスケールが合わない。連番画像で書き出すのであれば、書き出す枚数が固定でこれが解像度になる。つまり、高さ 500 で 500枚の連番画像ならば、各画像の解像度 500x500 になるようにする必要がある。  </p>

<p>あとは、ライノでメッシュのコンターで切断面を導出するならば、色指定は別の方法になる。前者でマスク、後者で色、これらを重ね合わせる。マスクの方の連番画像は、2値化させておくのが安全。マスクを Processing で数理的につくったので、明日は色を重ねて、出力データを作る。  </p>

