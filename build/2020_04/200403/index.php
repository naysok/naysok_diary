<h3>雑感</h3>

<p>目覚ましをかけて朝起きた。フルカラープリントに向けて必要な工程、テストデータを考えつつ、用意をして池尻へ。荷物を受け取り、FabCafe に寄る。池尻に戻って、小野さんたちと世間話をした。  </p>

