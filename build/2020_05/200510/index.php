<h3>雑感</h3>

<p>昨日、マーチングキューブとメタボールについて調べた。値を持った格子点に対して領域を定義するためのマーチングキューブの実装を終えているので、今日はメタボールの算出するため計算式を書く。  </p>

<p>メタボールは、重みや、距離から値を計算する指数関数の式で、格子点の各点の値を計算する。格子点の値は、メタボールの中心点から格子点の距離で計算され、複数のメタボールであれば、それぞれの中心点と格子点すべてで算出し足し合わせる。この辺りで、2次元配列を1次元に置き換えたり、転置したり、ゴリゴリ計算する。メタボールの基準が点ではなく、線や Brep であれば、格子点との最短距離で計算する必要がありそうなのでまた今度。  </p>

