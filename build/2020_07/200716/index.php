<p>朝から渋谷ハンズへ。リーズロック、ワイヤー、カシメの吊りものセットを買うのは何度目かな。今回は、絵の印象に合わせてシルバーのダブルクリップを買ってみた。  </p>

<p>大判プロッターの調子が悪いようで、かじりついている人が困っていたが、その人が諦めたら直った。プリントできたので、A2 と A1 で設置してみると、A1 のほうが良かったので、印刷に時間がかかるが、A1 で。  </p>

