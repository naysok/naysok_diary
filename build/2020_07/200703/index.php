<h3>雑感</h3>

<p>日中は、機材の検証。閾値だけ流し込めば良い自動機能が便利だと思っていたけど、結局、マシンが読み込みやすい綺麗なデータというものを頑張って作るのが大事みたいな部分があり、それはそうかみたいな気持ち。  </p>

