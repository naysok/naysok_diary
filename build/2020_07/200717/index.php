<h3>雑感</h3>

<p>金曜日、今日明日は渋谷にて展示設営。  </p>

<p>今日も朝からハンズへの買い出しを終え次第、そのほかの設営を終わらせよう。  </p>

<h3>設営 Day 2</h3>

<p>ポスターではないものの設置のために、ボルトナットのセットと、ナイロンワッシャーを買いつつ、フリー寸法の穴のフラットバーを見つけて買った。これは良かったと思う。  </p>

<p>一気に手を動かすだけなので、一気に、データ作成し、印刷し、設置し、という感じ。  </p>

<p>データを作成しつつ、大判の印刷をしつつ、設置を進める。設置をしていると、データ作成が遅れ、印刷の方が遅れる。データを作成して印刷していると、設置ができない。ちょっと手放しにくい仕事が手元にためってしまって大変だった。  </p>

<p>ガラスのペイントは、ハッチングのように模様をつけていくほうが良いかと思っていたが、ゴリゴリ塗っても意外と迫力が出てよかったと思う。  </p>

<p>僕が自分の作業から手を離せなかったが、やり方を伝えると、水平を出したり、細かな設営を進めてもらったりしてくれた助っ人チームに感謝。  </p>

<p>綺麗な展示に仕上がったと思う、良かった。  </p>

