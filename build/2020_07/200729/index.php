<h3>Auto GUI Tool の開発</h3>

<p>自動化はスクリーン座標系で操作するので、まず、操作のスクリーン座標系を計算した後に一度ファイルに書き込み保存、その後、GUI ライブラリを用いたプログラムを実行する。  </p>

<p>中間のテキストファイルのフォーマットを上手く決めれず午前を溶かす。前工程で計算した値のメモリ上の配列を何とかしてテキストファイルに書き出す。後工程でテキストファイルを一度配列に乗せるようにした。冗長だが、中間ファイルを手で修正することもあるし、そんなに重いファイルはないので、一旦これで。  </p>


