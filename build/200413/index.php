<h3>ラズパイカメラ設計</h3>

<p>ラズパイカメラを、アルミフレームに固定するが、向き調整の仕方を考える。アルミフレームの端面のタップ穴加工で回転を含む直立できそうであるが、地面との固定がわからない。普通のアルミフレーム関連パーツではそのようなものは無く、ミスミの切削プレートを使えば出来るが、単価が高い。また、贅沢仕様にすればフレームを組んで固めて、フットベースで調整できるようにしたいが、これも含めて、規格品でカバーできないときに安くするのは難しい。  </p>

<p>他にも、アルミフレーム同士を自由な角度でジョイントするミスミのフリーアングルブラケットというものを見つけた。このブラケットをアルミフレームの端面に取り付けるには、先入れダブルジョイントを使う必要があり、アルミフレーム自体にブラインドジョイント専用穴あけのS穴追加工が必要らしい。機会があればやってみたい。  </p>

