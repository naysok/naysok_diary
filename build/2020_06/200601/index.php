<h3>雑感</h3>

<p>早朝に起きてしまって、プログラムを設計する。  </p>

<p>出勤する日だったので、電車で真面目にスマホでプログラムを読んでいたら車酔いしてしまった。途中で降りてホームで休んで、錦糸町まで JR でたどり着き、もう少し休んでなんとか池尻へ。  </p>

<p>成果物を良くするために僕がすぐ打てる施策をお試し。良くなる気がするがこれを本体に組み込むのはかなり手間だと思う。まあ多少なりとも良くなることは確実だが、そのために必要なタスクとしては激重になるやつである。。。こういうのまれに発生するのだけど頑張る以外の選択肢はないので、俺は真鍋大度や！、みたいに謎に気合を入れて頑張ろうと思う。  </p>

<p>システムとしてのバージョンアップに加えて、加工形状についても機械の特性に合わせて設計しないといけない気がするので少し気が重い。  </p>

