<h3>雑感</h3>

<p>渋谷へ。  </p>

<p>映像制作をしている方にハードウェア開発について相談を受ける。  </p>

<p>割とこれだったらうれしくないなと思っていたものが、全て当てはまっていたような感じがある。というか、MV はガチ殴り合いだと思っていて、MV というものは本当に皆が工夫していてよくできているものが多いので、粗削りでも新しい手法を新しい手法 = 表現だよみたいにある種割り切るか、完成度の高いものを作って気にならないように見せないか、など戦略がいると思った（MV 以外の映像表現もすごいと思いますが僕が見ていないので今回はこんな書き方をしてしまった）。  </p>

