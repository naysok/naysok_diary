<h1>200610</h1>

<h3>雑感</h3>

<p>渋谷へ。機材検証。  </p>

<p>デジタル工作機あるあるかもしれないが、電源を入れた際に、機械が有無をいわずに機械原点検出をしに自動で動くという挙動があり、組み立てて途中の付属のパーツに衝突し、一部破損してしまった。  </p>

<p>3Dプリンタでも結構このような挙動の機械は多いが、本当に怖いのでやめてほしいなと思う。 といっても、オプションで機械原点の検出を行うようにしておくと操作をし忘れる恐れがあるし、加工範囲などの編集時には機械原点を持っていないといけないので加工開始時に行うわけにもいかないし、どんな感じが正解なんだろうか。  </p>

<p>もし僕が開発者ならば、起動時に『原点検出を行います。衝突の恐れのあるものがないか確認し、確認後、検出実行のボタンを押してください』というようにするかな。  </p>

