<h3>雑感</h3>

<p>仕事で渋谷に。とある機材の検証をば。  </p>

<p>ハードウェアは、機械原点出しも手早くできて、よくできた機材のように見える（競合機種に比べても安定感があるらしい）。プリセットのフォントのデータもすごく質がよさそう。  </p>

<p>ソフトウェアとしては、いくつか疑問が残る点もあったが、ソフトウェアには値段が異なるいくつかのグレードを設けているというシステムの都合で技術的なことではないらしい。  </p>

<p>今後、検証すべき点をリストアップし、この日は終了。  </p>

<p>22時より池尻へ。  </p>

<p>僕が溜めていた仕事があったので徹夜は確定であったが、明日の実製作の様子を確認して場合によっては修正するなどの仕事もあったため、スタジオに泊ることに。  </p>

<p>見た目を美しく、軌跡としては交差なく、且つ、一筆書きを作るという地味に面倒な作業が多くなりそうだった。一筆書きを作るときにオフセット地獄になるので、Grasshopper だけでは厳しいところがある。結局、ガリガリ手で書き、最後に CageEdit で目指す形状に向けて変形させるという手法を選択したが、頭悪そうに見えて、今回の場合には意外と理にかなっているかなとも思う。  </p>

<p>なんというか、最後に頼れるのは己の脳みそと己の腕力でだけある。こうしたら勝てる的な己の引き出しが多くあれば死なない、的なことを最近はよく考えてしまう。ここは ""俺が"" マジで頑張る必要がある、ような瞬間に、ここ1年くらい何度も遭遇している気がするが、自分が成長していてプロジェクトの重いウエイトを預けてもらっているからだと思いたいな。  </p>

<p>また、内部構造として三角形のトラスが強そうに見えるが、一筆書きにはハニカムが理にかなっているという気がしている（未確認）。  </p>

