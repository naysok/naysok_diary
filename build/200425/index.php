<h3>雑感</h3>

<p>昼に起きた。  </p>

<p>200412 に、楽天で適当な家電の店に発注した使い捨てマスク（50枚入り、4380円）が届いた。当たり前だが、ぱっと見、汚れ等は無い。週 1.5 回外出するとして、5-6ヶ月位持つ計算になるが、そんな先ではなく早く収まってくれ。  </p>

<p>4日分の日記をまとめて書く。GitHub を見ながら生活を思い出す。思っているよりも進捗が少ない。コードを書いたりする稼動の時間とは別に、なんとなく構成を設計しながら、家事やネットサーフィンをしている時間が多いのでもう少しメリハリをつけたいと思った。  </p>

